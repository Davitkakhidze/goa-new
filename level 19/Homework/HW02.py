#კომენტარებით ახსენით თუ რას აკეთებს .upper(); .lower(); .capitalize(); .find() ფუნქციები.
# .upper() - გადააქცევს სტრინგს დიდი ასოებით
# .lower() - გადააქცევს სტრინგს პატარა ასოებით
# .capitalize() - პირველ ასოს გადააქცევს დიდით, დროული მნიშვნელობების მოძრავებით
# .find() - მოძებნის ოპერაცია, უკუშეუძლებლობით, მოძებნილი პოზიციის მოძებნას.